
public class FloatDataTypeExample {

	public static void main(String[] args) {
	Float Number1 = 10.5f;
	Float Number2 = 4.2f;
	
	float additionresult = Number1 + Number2;
	System.out.println("Addition of float numbers" +Number1+ "and" +Number2+ "is" +additionresult);
	
	float substractionresult = Number1- Number2;
	System.out.println("Substraction of float numbers" +Number1+ "and" +Number2+ "is" +substractionresult);
	
	float multiplicationresult = Number1*Number2;
	System.out.println("multiplication of float numbers" +Number1+ "and" +Number2+ "is" +multiplicationresult);
	
	float divisionresult = Number1/ Number2;
	System.out.println("division of float numbers" +Number1+ "and" +Number2+ "is" +divisionresult);
	
	
	}

}
