package practice.examples.whiledowhile;

public class DoWhileExample4 {

	public static void main(String[] args) {
		
		// print numbers from 1 to 10 using do while 
		
		int number = 11;
		do {
			System.out.println(number);
			number = number + 1; // controlling the condition variable
		}while(number <= 10);
		
		System.out.println("End of the program");
	}
}
